import AminSelector from "./SelectAmin.tsx";



function Placeholder() {
  return <div className="editor-placeholder">Enter some rich text...</div>;
}


const editorConfig = {
  // The editor theme
  theme: exampleTheme,
  // Handling of errors during update
  onError(error : any) {
    throw error;
  },
  // Any custom nodes go here
  nodes: [
    HeadingNode,
    ListNode,
    ListItemNode,
    QuoteNode,
    CodeNode,
    CodeHighlightNode,
    TableNode,
    TableCellNode,
    TableRowNode,
    AutoLinkNode,
    LinkNode
  ],
  namespace : ""
};




export default function Editor({children, updateText, setUpdateText} : {children : any, updateText : any, setUpdateText : () => void }) {
  const { isDark, setText } : AppContextType  = useDashBoardContext();
  return (
    <LexicalComposer initialConfig={editorConfig } >

      <div className={ isDark ?  "editor-container " : "editor-container "}>
        <ToolbarPlugin  isDark={isDark} />
        <div className={isDark ? "editor-inner-dark" :"editor-inner" }  >
          <RichTextPlugin
            contentEditable={<ContentEditable className="editor-input" />}
            placeholder={<Placeholder />}
            ErrorBoundary={LexicalErrorBoundary}
          />
          <AminSelector  />
          <HistoryPlugin />
          <SuggestionUpdater updateText={updateText} setUpdateText={setUpdateText} />
          <OnChangePlugin onChange={ (_, s) => setText!(s.getRootElement()?.children) } /> {/*setText!(s.getRootElement()?.children) */}
          <AutoFocusPlugin />
          <CodeHighlightPlugin />
          <ListPlugin />
          <LinkPlugin />
          <AutoLinkPlugin />
          <ListMaxIndentLevelPlugin maxDepth={10} />
          <MarkdownShortcutPlugin transformers={TRANSFORMERS} />
        </div>
        {children}
      </div>



    </LexicalComposer>
  );
}
